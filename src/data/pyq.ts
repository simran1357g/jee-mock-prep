// Deterministic RNG so same seed → same paper (useful for share/re-attempt)
function mulberry32(seed: number) {
  return function () {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffle<T>(arr: T[], rand: () => number): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export interface GeneratedPaper {
  seed: number;
  exam: ExamType;
  questions: PYQ[]; // 25 per subject in NTA order: Physics, Chemistry, Math
  totalMarks: number;
  durationSec: number;
}

// Generate an NTA-style paper: 25 Physics, 25 Chemistry, 25 Math (75 total).
// If bank is smaller than 25 for a subject, questions repeat (bank extensible).
export function generatePaper(exam: ExamType, seed = Date.now()): GeneratedPaper {
  const rand = mulberry32(seed);
  const pick = (subject: Subject, count: number): PYQ[] => {
    const pool = PYQ_BANK.filter(
      (q) => q.subject === subject && (q.exam === exam || exam === "JEE Main"),
    );
    // fallback: if pool empty for Advanced-only, use any of that subject
    const source = pool.length > 0 ? pool : PYQ_BANK.filter((q) => q.subject === subject);
    const shuffled = shuffle(source, rand);
    const out: PYQ[] = [];
    for (let i = 0; i < count; i++) out.push(shuffled[i % shuffled.length]);
    return out;
  };
  const questions = [
    ...pick("Physics", 25),
    ...pick("Chemistry", 25),
    ...pick("Mathematics", 25),
  ];
  return {
    seed,
    exam,
    questions,
    totalMarks: 300, // +4/-1 style, 75×4
    durationSec: 2 * 60 * 60, // 2 hours
  };
}

// Generate a paper filtered by per-subject chapter selections.
// If a subject's chapter list is empty/undefined, all chapters are used.
export function generatePaperWithChapters(
  exam: ExamType,
  chapterFilter: Partial<Record<Subject, string[]>>,
  seed = Date.now(),
): GeneratedPaper {
  const rand = mulberry32(seed);
  const pick = (subject: Subject, count: number): PYQ[] => {
    const selected = chapterFilter[subject];
    let pool = PYQ_BANK.filter(
      (q) => q.subject === subject && (q.exam === exam || exam === "JEE Main"),
    );
    if (selected && selected.length > 0) {
      pool = pool.filter((q) => selected.includes(q.chapter));
    }
    // fallbacks so paper always fills
    if (pool.length === 0) {
      pool = PYQ_BANK.filter((q) => q.subject === subject);
      if (selected && selected.length > 0) {
        const filtered = pool.filter((q) => selected.includes(q.chapter));
        if (filtered.length > 0) pool = filtered;
      }
    }
    const shuffled = shuffle(pool, rand);
    const out: PYQ[] = [];
    for (let i = 0; i < count; i++) out.push(shuffled[i % shuffled.length]);
    return out;
  };
  const questions = [
    ...pick("Physics", 25),
    ...pick("Chemistry", 25),
    ...pick("Mathematics", 25),
  ];
  return {
    seed,
    exam,
    questions,
    totalMarks: 300,
    durationSec: 2 * 60 * 60,
  };
}

export function scorePaper(
  paper: GeneratedPaper,
  answers: Record<number, string>,
): { correct: number; wrong: number; unattempted: number; marks: number } {
  let correct = 0;
  let wrong = 0;
  let unattempted = 0;
  paper.questions.forEach((q, idx) => {
    const a = answers[idx];
    if (a === undefined || a === "") {
      unattempted++;
      return;
    }
    const normalize = (s: string) => s.trim().toLowerCase();
    if (normalize(a) === normalize(q.answer)) correct++;
    else wrong++;
  });
  const marks = correct * 4 - wrong * 1;
  return { correct, wrong, unattempted, marks };
}